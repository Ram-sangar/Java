public class Datatype{
	public static void main(String a[]){
		
		//boolean
		boolean a1=true;
		System.out.println("boolean values is: "+a1); 
		
		//byte
		byte b=127;
		System.out.println("byte values is: "+b);
		
		//character
		char c='a';
		System.out.println("character values is: "+c);
		
		//short
		short d=32;
		System.out.println("short values is: "+d);

		//integer
		int e=2147;
		System.out.println("Integer values is: "+e);
		
		//long
		long f=48454444;
		System.out.println("long values is: "+f);
		
		//float
		float g=1945;
		System.out.println("float values is: "+g);
		
		//double
		double h=12.545262626;
		System.out.println("double values is: "+h);
		
	}
	
}
		
		
		